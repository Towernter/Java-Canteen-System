        // TODO add your handling code here:
         if(username.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"USERNAME FIELD SHOULD NOT BE EMPTY");
        }
         else if(password.getText().equals("")){
              JOptionPane.showMessageDialog(this,"PASSWORD FIELD SHOULD NOT BE EMPTY");
         }
         else {
             try {
        connect("select * FROM `super_admin` WHERE name=? and password=?");
        ps.setString(1, username.getText());
        ps.setString(2,password.getText());
        rs=ps.executeQuery();
        
        if(rs.next())
        {
            Super s=new Super();
            s.show();
            this.dispose();
           
        }
        else
        {
            JOptionPane.showMessageDialog(null,"ACCOUNT DOES NOT EXIST");
            
        }     
    } catch (SQLException ex) {
      JOptionPane.showMessageDialog(null, ex);
    }
        
         }
